public class forLoopEg {

    public static void main(String args[]) {

    	int i;

    	//for loop
    	for (i =1;i<=10; i++){
    		System.out.println("Value of i is " + i);
    	}//end for

		//print blank line
		System.out.println();

    	System.out.println("\n\nAfter the loop the value of i is " + i);

    }//end main method


}//end class