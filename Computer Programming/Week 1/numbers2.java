public class numbers2 {

    public static void main(String args[]) {

    	//declare number variables
    	int x = 10;
    	int y = 5;

    	System.out.print(x + "+" + y + "=");
    	System.out.println(x+y);

    	System.out.print(x + "-" + y + "=");
    	System.out.println(x-y);

    	System.out.print(x + "*" + y + "=");
    	System.out.println(x*y);

    	System.out.print(x + "/" + y + "=");
    	System.out.println(x/y);

    }//end main method


}//end class