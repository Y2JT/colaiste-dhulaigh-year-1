public class ifEg {

    public static void main(String args[]) {

    	int age = 18;

    	if(age>17){
    		System.out.println("You can vote");
    	}
    	else{
    		System.out.println("You cannot vote");
    		System.out.println("You're too young");
    	}


    }//end main method


}//end class